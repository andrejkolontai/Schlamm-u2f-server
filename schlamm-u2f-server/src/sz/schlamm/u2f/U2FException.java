package sz.schlamm.u2f;

public class U2FException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3423358227187480162L;

	public U2FException() {
		super();
	}

	public U2FException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public U2FException(String arg0) {
		super(arg0);
	}

	public U2FException(Throwable arg0) {
		super(arg0);
	}

}
